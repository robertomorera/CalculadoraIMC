public class IMCException extends Exception {

    /*
     Constructor de la excepción IMCException.
     */
    public IMCException(String message) {
        super(message);
    }

    /*
     Devuelve el mensaje de la excepción.
     */
    @Override
    public String getMessage() {
        return super.getMessage();
    }
}
